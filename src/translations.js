function getNested(obj, key) {
  const parts = key.split('.');
  let cur = obj;
  for (const p of parts) {
    if (cur == null) return key;
    cur = cur[p];
  }
  return cur != null ? cur : key;
}

export function t(lang, key) {
  const data = TRANSLATIONS[lang] || TRANSLATIONS.en;
  return getNested(data, key);
}

/** Anclas por idioma para que la URL coincida con el idioma (Home → #home, Inicio → #inicio) */
export const SECTION_HASH = {
  en: { home: 'home', about: 'about-us', services: 'services', how: 'how-we-work', quote: 'quote' },
  fr: { home: 'accueil', about: 'a-propos', services: 'services', how: 'notre-processus', quote: 'devis' },
  es: { home: 'inicio', about: 'nosotros', services: 'servicios', how: 'como-funciona', quote: 'cotizacion' }
};

export function getSectionHash(lang, sectionKey) {
  const l = SECTION_HASH[lang] ? lang : 'en';
  return SECTION_HASH[l][sectionKey] ?? sectionKey;
}

const HASH_TO_SECTION = {};
Object.keys(SECTION_HASH).forEach((lang) => {
  Object.entries(SECTION_HASH[lang]).forEach(([key, hash]) => { HASH_TO_SECTION[hash] = key; });
});
export function getSectionKeyFromHash(hash) {
  return hash ? HASH_TO_SECTION[hash.replace(/^#/, '')] : null;
}
